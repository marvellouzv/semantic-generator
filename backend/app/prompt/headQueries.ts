// Текст-инварианты (часть system-инструкции)
const SYSTEM_BASE = (locale: string) => `
Ты — старший SEO-стратег и эксперт по кластеризации для российского рынка (Google RU + Яндекс).
Специализация: извлечение «верхнеуровневых» head-запросов (ВЧ/СЧ ядро, без длинных хвостов) для дальнейшей декомпозиции.
Рабочая локаль: ${locale}.
Всегда соблюдай формат OUTPUT (STRICT) и правила нормализации ниже.
`;

// Динамический блок по выбранным типам запросов
function buildTypeDirectives(settings: GenSettings): string {
  const selected = new Set(settings.types);
  const all: QueryType[] = [
    "Коммерческие","Информационные","Сервисные","Ценовые","Локальные","Срочные",
    "Отзывы","Сравнительные","Своими руками","Загрузки","Технические","Правовые",
    "Брендовые","Навигационные","Проблемные"
  ];
  const disallowed = all.filter(t => !selected.has(t));

  const allowBlock = Array.from(selected)
    .map(t => `### ${t}\n${TYPE_RULES[t].trim()}`)
    .join("\n");

  const brandRule =
    selected.has("Брендовые")
      ? `- Брендовые кластеры разрешены ТОЛЬКО из whitelist: ${(settings.brandWhitelist ?? []).join(", ") || "— (пусто, бренды не выводить)"}.\n`
      : `- Все брендовые запросы запрещены.\n`;

  return `
РАЗРЕШЕНЫ ТОЛЬКО следующие типы запросов (остальные исключить):
${Array.from(selected).join(", ") || "— (ничего не выбрано)"}.

${brandRule}
Дополнительные правила по разрешённым типам:
${allowBlock}

Запрещённые типы (НЕ генерировать): ${disallowed.join(", ") || "—"}.
`.trim();
}

// Фиксированный корпус правил (часть user-инструкции)
const FIXED_RULES = `
CONSTRAINTS & DEFINITIONS
- «Верхнеуровневые» = короткие, наиболее частотные, обобщающие формулировки (обычно 1–3 слова, иногда 4).
- Исключать длинные хвосты, уточнения и геомодификаторы (город/район/«рядом»), кроме случаев, когда это разрешено блоком типов.
- Исключать бренды, если не разрешено явно (см. блок типов).
- Нормализовать морфологию и синонимы; в Head Query оставлять каноническую форму.
- Дедупликация: объединять дублеты (ед./мн., порядок слов, опечатки, транслит), оставлять канон.
- Интент: informational / commercial / transactional / navigational.
- Рынок: Россия/СНГ. Язык: русский.

SCOPE & COVERAGE
- Выдать максимально возможное количество head-запросов для полного покрытия тематики (без искусственных лимитов).
- Организовать в такое количество Parent Theme, сколько нужно для полного покрытия; внутри — сортировка по ожидаемой частоте/ценности.
- Не расширять искусственно редкими формулировками: фокус на «ядре» спроса.

QUALITY RULES
- Ясность: лаконичные, общеупотребимые формулировки без хвостов.
- Полнота: покрыть все очевидные ветви спроса.
- Чистота: удалить нерелевантные/бытовые запросы.
- Финальный контроль: убрать дубликаты, добавить пропущенные очевидные вершины.

OUTPUT (STRICT)
1) Сначала одним списком выведи Parent Theme (только названия категорий), через запятую.
2) Затем ОДНА Markdown-таблица со столбцами ровно:
   - Head Query
   - Intent
   - Demand Level
   - Parent Theme
   Без дополнительных столбцов/примечаний/пояснений.
3) Не выводи plain-text список и пост-скриптумы.

STEP-BY-STEP
1) Если тема однозначна — сразу работай (без вопросов).
2) Определи столько Parent Theme, сколько нужно (закрыть все ключевые ветви спроса).
3) Для каждой Parent Theme сгенерируй столько head-запросов, сколько нужно (1–3 слова; в исключениях до 4).
4) Нормализуй/дедуплицируй, оставь канон.
5) Проставь Intent и Demand Level (High/Medium/Low) по здравому смыслу.
6) Отсортируй внутри каждой Parent Theme по приоритету.
7) Выведи результат строго по OUTPUT (STRICT).
`.trim();

// Сборка финальных сообщений для API/Cursor
export function buildHeadQueriesPrompt(settings: GenSettings) {
  const locale = settings.locale ?? "ru-RU";
  const system = [SYSTEM_BASE(locale)].join("\n").trim();

  const typeDirectives = buildTypeDirectives(settings);

  const user = `
Act like a senior SEO strategist and keyword clustering expert (Google RU + Яндекс).
You specialize in extracting "верхнеуровневые" head queries (ВЧ/СЧ «ядро», без длинных хвостов).

OBJECTIVE
Сгенерировать максимально полный и релевантный список ВЧ-запросов верхнего уровня для заданной тематики (на русском), который станет «верхушками кластеров». Модель сама определяет главные запросы и возвращает их строго в формате OUTPUT (STRICT).

INPUT
- Тематика: "${settings.topic}"

TYPE FILTER (динамический, зависит от UI)
${typeDirectives}

${FIXED_RULES}
`.trim();

  return { system, user };
}
