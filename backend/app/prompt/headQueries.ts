// Генератор динамического промпта под выбор "Типов запросов" из UI.
// Совместим с OpenAI Responses API и Cursor. Локаль по умолчанию ru-RU.

export type QueryType =
  | "Коммерческие"
  | "Информационные"
  | "Сервисные"
  | "Ценовые"
  | "Локальные"
  | "Срочные"
  | "Отзывы"
  | "Сравнительные"
  | "Своими руками"
  | "Загрузки"
  | "Технические"
  | "Правовые"
  | "Брендовые"
  | "Навигационные"
  | "Проблемные";

export interface GenSettings {
  topic: string;               // Тематика: короткая строка
  locale?: string;             // "ru-RU" по умолчанию
  types: QueryType[];          // Выбранные типы из UI
  brandWhitelist?: string[];   // (опционально) допустимые бренды, если включен тип "Брендовые"
}

